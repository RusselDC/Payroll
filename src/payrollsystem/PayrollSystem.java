    private void wageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_wageActionPerformed
        // TODO add your handling code here:
        try {
            String id = Search.getText();
            String HW = Hourswork.getText();
            String HR = HourlyRate.getText();
            String T = Taxes.getText();
            String A = Allowance.getText();
            String D = ddd.getText();
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/payroll","root","");
            PreparedStatement pst = con.prepareStatement("UPDATE  employee set HoursWorked=?,HourlyRate=?,Taxes=?,Allowance=?,Deductions=? where EmpID=?");
            pst.setString(1, HW);
            pst.setString(2, HR);
            pst.setString(3, T);
            pst.setString(4, A);
            pst.setString(5, D);
            pst.setString(6, id);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Update successful.");
        } catch (ClassNotFoundException ex) {
            java.util.logging.Logger.getLogger(EmpUpdate.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(EmpUpdate.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_wageActionPerformed

