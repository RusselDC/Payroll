    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        try{
            pst = con.prepareStatement("SELECT EmpName,Address,Contact,Email,Position,HoursWorked,HourlyRate,Taxes,Allowance,Deductions FROM `employee` WHERE EmpID =?");
            int id = Integer.parseInt(Search.getText());
            pst.setInt(1, id);
            ResultSet rs1 = pst.executeQuery();
            if(rs1.next()==false)
            {
                JOptionPane.showMessageDialog(this, "Sorry, record not found.");
                Name.setText("");
                Address.setText("");
                Contact.setText("");
                Email.setText("");
                Position.setText("");
                
            }
            else 
            {
                Name.setText(rs1.getString("EmpName"));
                Address.setText(rs1.getString("Address"));
                Contact.setText(rs1.getString("Contact"));
                Email.setText(rs1.getString("Email"));
                Position.setText(rs1.getString("Position"));
                Hourswork.setText(rs1.getString("HoursWorked"));
                HourlyRate.setText(rs1.getString("HourlyRate"));
                Taxes.setText(rs1.getString("Taxes"));
                Allowance.setText(rs1.getString("Allowance"));
                ddd.setText(rs1.getString("Deductions"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(PayrollSystem.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_searchActionPerformed

